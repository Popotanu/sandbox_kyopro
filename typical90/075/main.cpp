ll N;
ll count_exp(ll n) {
  ll result = 0;
  ll n_cur = n;

  for (int i = 2; 1LL * i * i <= n; i++) {
    ll ex = 0;
    while (n_cur % i == 0) {
      ex++;
      n_cur /= i;
    }
    result += ex;
  }

  if (n_cur != 1) result++;
  return result;
}
void _main() {
  cin >> N;

  // 各素因数を頂点とした2分木を考えて,その高さが答え...なはず
  // 計算に必要なのは指数の情報だけ.
  std::cout << ceil(log2(count_exp(N))) << std::endl;
}
